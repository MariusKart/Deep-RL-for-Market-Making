def discount_rl(arrival_rates: np.ndarray | None = None) -> float:
    lam = ARRIVAL_RATES if arrival_rates is None else np.asarray(arrival_rates, dtype=float)
    return float(2.0 * np.sum(lam) / (RF + 2.0 * np.sum(lam)))


DISCOUNT_RL = discount_rl()


def subset_array(arr: np.ndarray, bonds: list[int] | tuple[int, ...] | np.ndarray) -> np.ndarray:
    bonds = np.asarray(bonds, dtype=int)
    return np.asarray(arr, dtype=float)[bonds].copy()


def subset_cov(bonds: list[int] | tuple[int, ...] | np.ndarray) -> np.ndarray:
    bonds = np.asarray(bonds, dtype=int)
    return np.asarray(COV[np.ix_(bonds, bonds)], dtype=float).copy()


def subset_f_parameters(
    bonds: list[int] | tuple[int, ...] | np.ndarray,
) -> tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    bonds = np.asarray(bonds, dtype=int)
    return (
        ALPHA[bonds].copy(),
        BETA[bonds].copy(),
        MU[bonds].copy(),
        SCALE[bonds].copy(),
    )


def bond_signature(bonds: list[int] | tuple[int, ...] | np.ndarray) -> str:
    bonds = list(map(int, bonds))
    return "_".join(f"{i:02d}" for i in bonds)